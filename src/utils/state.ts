//------------------------------------------------------------------------------
// State Updater
//------------------------------------------------------------------------------

export type StateUpdater<T> = (prev: T) => T;

//------------------------------------------------------------------------------
// State Update
//------------------------------------------------------------------------------

export type StateUpdate<T> = T | StateUpdater<T>;

//------------------------------------------------------------------------------
// State Setter
//------------------------------------------------------------------------------

export type StateSetter<T> = (update: StateUpdate<T>) => T;

//------------------------------------------------------------------------------
// Is Updater
//------------------------------------------------------------------------------

export function isStateUpdater<T>(
  maybeUpdater: StateUpdate<T>,
): maybeUpdater is StateUpdater<T> {
  return typeof maybeUpdater === "function";
}
