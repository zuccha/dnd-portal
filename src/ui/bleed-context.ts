import { createContext, useContext } from "react";

//------------------------------------------------------------------------------
// Bleed Corner
//------------------------------------------------------------------------------

export type BleedCorner = {
  color: string;
  length: number;
};

//------------------------------------------------------------------------------
// Bleed
//------------------------------------------------------------------------------

export type Bleed = {
  corner?: BleedCorner;
  x: number;
  y: number;
};

//------------------------------------------------------------------------------
// Bleed Context
//------------------------------------------------------------------------------

export const BleedContext = createContext<Bleed>({ x: 0, y: 0 });

//------------------------------------------------------------------------------
// Use Bleed
//------------------------------------------------------------------------------

export function useBleed() {
  return useContext(BleedContext);
}
