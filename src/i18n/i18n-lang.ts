import { z } from "zod";
import { createLocalStore } from "../store/local-store";

//------------------------------------------------------------------------------
// I18n Lang
//------------------------------------------------------------------------------

export type I18nLang = string;

//------------------------------------------------------------------------------
// I18n Lang Context
//------------------------------------------------------------------------------

export type I18nLangContext = Record<string, Record<I18nLang, string>>;

//------------------------------------------------------------------------------
// I18n Lang Store
//------------------------------------------------------------------------------

export const i18nLangStore = createLocalStore(
  "i18n.lang",
  "en",
  z.string().parse
);

//------------------------------------------------------------------------------
// Use I18n Lang
//------------------------------------------------------------------------------

export const useI18nLang = i18nLangStore.use;
